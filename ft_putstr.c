#include "ft_printf.h"

size_t	ft_putstr(char *s)
{
	int		i;
	size_t	cmp;

	i = 0;
	cmp = 0;
	if (!s)
	{
		write(1, "(null)", 6);
		return (6);
	}
	else
	{
		while (s[i] != '\0')
		{
			write(1, &s[i], 1);
			cmp++;
			i++;
		}
	}
	return (cmp);
}
